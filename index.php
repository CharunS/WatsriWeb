    <script type="text/javascript">
      $(document).ready(function() {
          $('#errMsgUserName').hide();
          $('#errMsgPassword').hide();
          $('#errMsgDataInvalid').hide();
      });

      $('#btnLogin').click(function() {
          let userNameObj = $('#userName').val();
          let passWordObj = $('#passWord').val();

          if (userNameObj == '' && passWordObj == ''){
            $('#errMsgUserName').show();
            $('#errMsgPassword').show();
            $('#userName').focus();
            return;
          }
          else if(userNameObj == ''){
            $('#errMsgUserName').show();
            $('#userName').focus();
            return;
          }
          else if(passWordObj == ''){
            $('#errMsgPassword').show();
            $('#passWord').focus();
            return;
          }
          else{
            if (userNameObj.toUpperCase() != 'WATSRI' && passWordObj.toUpperCase() != 'WATSRI'){
                $('#errMsgDataInvalid').show();
                $('#userName').focus();
            }
            else{
              sessionStorage.setItem("UserName", "WATSRI");
              sessionStorage.setItem("PassWord", "WATSRI");
              // window.location = "http/20.188.103.18/TimeSheetMaintenance/UserMaintenance.php";
              window.location.href = 'http://20.188.103.18/eec04';
            }
          }
      });

      $('#loginModal').on('shown.bs.modal', function () {
          $('#userName').focus();
      });

      $('#loginModal').on('hidden.bs.modal', function () {
          $('#errMsgUserName').hide();
          $('#errMsgPassword').hide();
          $('#errMsgDataInvalid').hide();
          $('#userName').val('');
          $('#passWord').val('');
      });

      $('#userName').change(function() {
        if(!$(this).val() == '' || !$(this).val() == ''){
          $('#errMsgUserName').hide();
        }
      });

      $('#userName').on('keypress',function(e) {
        if(e.keyCode == 13){
          $('#btnLogin').trigger('click');
        }
      });

      $('#passWord').change(function() {
        if(!$(this).val() == '' || !$(this).val() == ''){
          $('#errMsgPassword').hide();
        }
      });

      $('#passWord').on('keypress',function(e) {
        if(e.keyCode == 13){
          $('#btnLogin').trigger('click');
        }
      });
    </script>

